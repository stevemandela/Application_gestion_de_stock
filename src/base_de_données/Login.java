    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        rs = db.querySelectAll("utilisateur", "username='" + txt_username.getText() + "' and password='" + txt_password.getText() + "'");
        try{
            while (rs.next()){
                username1 = rs.getString("username");
                password1 = rs.getString("password");
                hak = rs.getString("type");
            }
        } 
            catch(SQLException ex){
                    Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                    }
        if(username1 == null && password1 == null){ 
            JOptionPane.showMessageDialog(this, "le nom utilisateur ou le mot de passe est incorrect");
            jlabel.setText("erreeeeeeeuuuuuurrrr");
            jlabel.setVisible(true);
        }else{
            if(hak.equals("directeur")){
                Principale h = new Principale();
                h.setVisible(true);
                this.dispose();//si on a le directeur le programme ouvre la page principale
            }else{
                Caisier k = new Caisier();//si on a le caisier le programme ouvre jframe caisier
                k.setVisible(true);
               // this.dispose();//fonction dispose pour fermer
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

